namespace Day17
{
    internal struct SpaceCube
    {
        public int x;
        public int y;
        public int z;
        public int w;

        public SpaceCube(int x, int y, int z, int w) : this()
        {
            this.x = x;
            this.y = y;
            this.z = z;
            this.w = w;
        }
    }
}